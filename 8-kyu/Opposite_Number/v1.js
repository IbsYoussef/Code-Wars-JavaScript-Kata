/*
Kata: Opposite Number
Link:
Description: Very simple, given a number (integer / decimal / both depending on the language), find its opposite (additive inverse).

Examples:

1: -1
14: -14
-34: 34
*/

const opposite = (number) => -number;

// Test cases
console.log(opposite(1)); // -1
console.log(opposite(14)); // -14
console.log(opposite(-34)); // 34
